package com.achieve.model;

public class AchieveVO implements java.io.Serializable{
	private String ach_No;
	private String ach_Name;
	private byte[] ach_Picture;
	private String ach_Explain;
	
	
	public String getAch_No() {
		return ach_No;
	}
	public void setAch_No(String ach_No) {
		this.ach_No = ach_No;
	}
	public String getAch_Name() {
		return ach_Name;
	}
	public void setAch_Name(String ach_Name) {
		this.ach_Name = ach_Name;
	}
	public byte[] getAch_Picture() {
		return ach_Picture;
	}
	public void setAch_Picture(byte[] ach_Picture) {
		this.ach_Picture = ach_Picture;
	}
	public String getAch_Explain() {
		return ach_Explain;
	}
	public void setAch_Explain(String ach_Explain) {
		this.ach_Explain = ach_Explain;
	}
	
	
}
